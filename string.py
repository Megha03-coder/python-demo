# string is a Datatype - sequence of characters

str="megha chauhan"
print(str)
print(str[3])        #print character of index 3 


print(str[3:6])     # print characters from index 3 to 5 
print(str[:9])       # starting index by default include


# Methods of string

print(str.endswith("a"))
print(str.capitalize())

print(str.replace("chauhan","singh"))
print(str.find("h"))
print(str.count("chauhan"))
